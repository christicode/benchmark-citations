def w_of(r):
    """Source weight for a citation record: blog_headliner 3, model_card 2, system_card 1."""
    return points(r.get("weight_class"))


def blink(canon):
    """Clickable benchmark -> its citation records on GitHub (code search surfaces the
    data/citations.jsonl lines + the data/extractions/*.yaml entries for this name)."""
    query = "repo:" + REPO + " " + Q + canon + Q
    return "https://github.com/search?q=" + urllib.parse.quote(query) + "&type=code"


def load():
    return [json.loads(l) for l in open(ROOT / "data" / "citations.jsonl") if l.strip()]


def _build_coverage(rows, ROOT, e):
    """Coverage matrix page: the SUPERSET of every benchmark across three sources -
    our citations, Harbor's adapters/ folder, and Harbor's registry.json - shown as columns.
    Identity is joined across the three name-spaces (case/underscore/suffix drift) so the
    same benchmark lines up on one row."""
    try:
        snap = json.loads((ROOT / "data" / "harbor_adapters.json").read_text())
    except Exception:
        snap = {"adapters": [], "registry": [], "fetched_at": "?"}

    def norm(s):
        return "".join(ch for ch in (s or "").lower() if ch.isalnum())

    cite = {}
    for r in rows:
        c = r.get("benchmark_canonical")
        if not c:
            continue
        d = cite.setdefault(c, {"n": 0, "w": 0.0, "labs": set(), "htype": None,
                                "hadapter": None, "hname": None})
        d["n"] += 1; d["w"] += w_of(r); d["labs"].add(r["citing_lab"])
        d["htype"] = r.get("harbor_type") or d["htype"]
        d["hadapter"] = r.get("harbor_adapter") or d["hadapter"]
        d["hname"] = r.get("harbor_name") or d["hname"]

    rows_by_uid = {}
    def row(uid):
        return rows_by_uid.setdefault(uid, {"display": None, "cite": None, "adapter": None,
                                            "reg_name": None, "reg_vers": None})
    reg_uid, ad_uid = {}, {}
    for rg in snap.get("registry", []):
        uid = "reg::" + norm(rg["name"]); R = row(uid)
        R["reg_name"] = rg["name"]; R["reg_vers"] = rg.get("versions") or []
        R["display"] = R["display"] or rg["name"]; reg_uid[norm(rg["name"])] = uid
    for ad in snap.get("adapters", []):
        nm = ad["name"]; rn = ad.get("registry_name")
        if rn and norm(rn) in reg_uid:
            uid = reg_uid[norm(rn)]
        elif norm(nm) in reg_uid:
            uid = reg_uid[norm(nm)]
        else:
            uid = "ad::" + norm(nm); row(uid)["display"] = row(uid)["display"] or nm
        row(uid)["adapter"] = nm; ad_uid[nm] = uid
    for c, d in cite.items():
        uid = None
        if d["htype"] in ("native", "fork", "adapter"):   # never link a false_positive to its collided entry
            if d["hname"] and norm(d["hname"]) in reg_uid:
                uid = reg_uid[norm(d["hname"])]
            elif d["hadapter"] and d["hadapter"] in ad_uid:
                uid = ad_uid[d["hadapter"]]
            elif d["hadapter"] and norm(d["hadapter"]) in reg_uid:
                uid = reg_uid[norm(d["hadapter"])]
        if uid is None:
            uid = "cite::" + c
        R = row(uid); R["cite"] = d; R["display"] = c

    def nsrc(R):
        return (1 if R["cite"] else 0) + (1 if R["adapter"] else 0) + (1 if R["reg_name"] else 0)
    ordered = sorted(rows_by_uid.values(),
                     key=lambda R: (-nsrc(R), -(R["cite"]["w"] if R["cite"] else 0), (R["display"] or "")))
    tot = len(ordered)
    n_c = sum(1 for R in ordered if R["cite"])
    n_a = sum(1 for R in ordered if R["adapter"])
    n_r = sum(1 for R in ordered if R["reg_name"])
    n_all3 = sum(1 for R in ordered if nsrc(R) == 3)
    n_citeonly = sum(1 for R in ordered if R["cite"] and not R["adapter"] and not R["reg_name"])

    YES = "<span class=y>\u2713</span>"; NO = "<span class=n>\u00b7</span>"
    AURL = "https://github.com/harbor-framework/harbor/tree/main/adapters/"

    def htag(R):
        ht = R["cite"]["htype"] if R["cite"] else None
        if ht and ht != "none":
            return f"<span class=tag>{e(ht)}</span>"
        if ht == "none" and not R["adapter"] and not R["reg_name"]:
            return "<span class=no>not in harbor</span>"
        if R["adapter"]:
            return "<span class=tag>adapter</span>"
        if R["reg_name"]:
            return "<span class=tag>native/registry</span>"
        return EMDASH

    H = ["<!doctype html><html lang=en><head><meta charset=utf-8>",
         "<meta name=viewport content='width=device-width,initial-scale=1'>",
         "<title>PaperTrail \u00b7 coverage matrix</title>", """<style>
    :root{--bg:#ffffff;--panel:#eef3fb;--b:#d7e3f2;--fg:#586e75;--emph:#073642;--mut:#93a1a1;
      --blue:#268bd2;--green:#22c55e;--red:#dc322f;--yellow:#b58900}
    *{box-sizing:border-box}
    body{margin:0;background:var(--bg);color:var(--fg);font:13px/1.5 "JetBrains Mono","SF Mono",ui-monospace,Menlo,Consolas,monospace}
    .wrap{max-width:1180px;margin:0 auto;padding:22px}
    h1{font-size:21px;color:var(--emph);margin:0 0 2px}
    a{color:var(--blue);text-decoration:none}a:hover{text-decoration:underline}
    .sub{color:var(--mut);font-size:12px;margin:0 0 12px}
    .cards{display:flex;flex-wrap:wrap;gap:8px;margin:12px 0}
    .kpi{background:var(--panel);border:1px solid var(--b);border-radius:6px;padding:7px 11px;min-width:96px}
    .kpi b{font-size:17px;display:block;color:var(--emph)}
    .bar{margin:14px 0 6px}
    .bar input{font:13px ui-monospace,monospace;padding:5px 8px;border:1px solid var(--b);border-radius:6px;width:230px}
    .bar button{font:12px ui-monospace,monospace;padding:5px 9px;margin-left:5px;border:1px solid var(--b);
      background:var(--panel);color:var(--fg);border-radius:6px;cursor:pointer}
    .bar button.on{background:var(--blue);color:#fff;border-color:var(--blue)}
    table{width:100%;border-collapse:collapse;background:var(--panel);border:1px solid var(--b);border-radius:6px;overflow:hidden}
    th,td{padding:5px 9px;text-align:left;border-bottom:1px solid var(--b);vertical-align:top;font-size:12.5px}
    th{color:var(--mut);font-weight:700;font-size:10.5px;text-transform:uppercase;letter-spacing:.02em;cursor:pointer}
    tr:last-child td{border-bottom:0}td.c,th.c{text-align:center}
    code{color:var(--blue);font-weight:600}
    .y{color:var(--green);font-weight:700}.n{color:var(--b)}
    .tag{color:var(--emph);background:#dbe7f5;border:1px solid var(--b);border-radius:4px;padding:0 5px;font-size:11px}
    .no{color:var(--red)}
    .foot{color:var(--mut);font-size:11.5px;margin:16px 0 0}
    </style></head><body><div class=wrap>"""]
    H.append("<h1>Coverage matrix</h1>")
    H.append("<p class=sub><a href='index.html'>\u2190 back to dashboard</a></p>")
    H.append("<div class=cards>"
             f"<div class=kpi><b>{tot}</b>total benchmarks</div>"
             f"<div class=kpi><b>{n_c}</b>in citations</div>"
             f"<div class=kpi><b>{n_a}</b>in adapters/</div>"
             f"<div class=kpi><b>{n_r}</b>in registry</div>"
             f"<div class=kpi><b>{n_all3}</b>in all three</div>"
             f"<div class=kpi><b>{n_citeonly}</b>cited, not in Harbor</div>"
             "</div>")
    H.append("<div class=bar><input id=q placeholder='filter benchmarks...' oninput='F()'>"
             "<button class=on data-f=all onclick='P(this)'>all</button>"
             "<button data-f=cite onclick='P(this)'>in citations</button>"
             "<button data-f=adapter onclick='P(this)'>in adapters</button>"
             "<button data-f=registry onclick='P(this)'>in registry</button>"
             "<button data-f=citeonly onclick='P(this)'>cited, not in Harbor</button>"
             "<button data-f=harboronly onclick='P(this)'>Harbor, not cited</button>"
             "<button data-f=all3 onclick='P(this)'>in all three</button></div>")
    H.append("<table id=t><thead><tr><th onclick='S(0)'>benchmark</th>"
             "<th class=c onclick='S(1)'>citations</th><th class=c onclick='S(2)'>adapters/ folder</th>"
             "<th class=c onclick='S(3)'>registry</th><th onclick='S(4)'>harbor</th></tr></thead><tbody>")
    for R in ordered:
        disp = R["display"] or "?"
        c = R["cite"]
        if c:
            cc = (f"{YES} <a href='https://github.com/search?q=" +
                  "repo:christicode/benchmark-citations+%22" + e(disp) + "%22&type=code' target=_blank>"
                  f"{c['n']} cites</a> <span class=mut>{c['w']:.1f}w</span>")
            cval = c["w"]
        else:
            cc = NO; cval = -1
        if R["adapter"]:
            ac = f"{YES} <a href='{AURL}{e(R['adapter'])}' target=_blank><code>{e(R['adapter'])}</code></a>"; aval = 1
        else:
            ac = NO; aval = 0
        if R["reg_name"]:
            vers = ",".join(str(v) for v in (R["reg_vers"] or []))
            rc = f"{YES} <code>{e(R['reg_name'])}</code>@{e(vers)}"; rval = 1
        else:
            rc = NO; rval = 0
        dc = "1" if c else "0"; da = "1" if R["adapter"] else "0"; dr = "1" if R["reg_name"] else "0"
        H.append(f"<tr data-c={dc} data-a={da} data-r={dr} data-name='{e(disp.lower())}' "
                 f"data-s='{cval:.1f}|{aval}|{rval}'><td><code>{e(disp)}</code></td>"
                 f"<td class=c>{cc}</td><td class=c>{ac}</td><td class=c>{rc}</td><td>{htag(R)}</td></tr>")
    H.append("</tbody></table>")
    H.append("<p class=foot>christicode/benchmark-citations \u00b7 Harbor snapshot in data/harbor_adapters.json \u00b7 "
             "\u2713 = present in that source. 'harbor' column: adapter / native-registry / not-in-harbor.</p>")
    H.append("""<script>
    var FT='all';
    function P(b){FT=b.dataset.f;document.querySelectorAll('.bar button').forEach(function(x){x.classList.remove('on')});b.classList.add('on');F()}
    function F(){var q=document.getElementById('q').value.toLowerCase();
      document.querySelectorAll('#t tbody tr').forEach(function(tr){
        var c=tr.dataset.c=='1',a=tr.dataset.a=='1',r=tr.dataset.r=='1';
        var ok=({all:1,cite:c,adapter:a,registry:r,citeonly:c&&!a&&!r,harboronly:(a||r)&&!c,all3:c&&a&&r})[FT];
        var m=tr.dataset.name.indexOf(q)>=0;
        tr.style.display=(ok&&m)?'':'none';});}
    function S(i){var tb=document.querySelector('#t tbody');var rs=[].slice.call(tb.children);
      rs.sort(function(x,y){var a,b;
        if(i==0){a=x.dataset.name;b=y.dataset.name;return a<b?-1:a>b?1:0}
        var xs=x.dataset.s.split('|'),ys=y.dataset.s.split('|');
        a=parseFloat(xs[i-1]);b=parseFloat(ys[i-1]);return b-a});
      rs.forEach(function(t){tb.appendChild(t)});}
    </script>""")
    H.append("</div></body></html>")
    (ROOT / "docs" / "coverage.html").write_text("\n".join(H))
    return tot, n_c, n_a, n_r


def main():
    rows = load()
    dated = [r["source_doc"]["pub_date"] for r in rows if r["source_doc"].get("pub_date")]
    asof = max([datetime.date.today().isoformat()] + dated)
    ad = datetime.date.fromisoformat(asof)
    w90 = (ad - datetime.timedelta(days=90)).isoformat()
    w180 = (ad - datetime.timedelta(days=NEW_DAYS)).isoformat()
    news_cut = (ad - datetime.timedelta(days=7)).isoformat()

    def blank():
        return {"w": 0.0, "cites": 0, "cards": 0, "blogs": 0, "docs": set(), "labs": set(), "type": "?", "domain": "?", "status": "not_in_harbor",
                "on": False, "first": None, "recent": 0, "model_pct": {}, "method": False}
    agg = collections.defaultdict(blank)
    cov = {k: {"wt": 0.0, "won": 0.0, "wrev": 0.0, "n": 0, "non": 0,
               "ag_t": 0.0, "ag_o": 0.0, "st_t": 0.0, "st_o": 0.0, "ag_n": 0, "st_n": 0}
           for k in ("all", "180", "90")}

    def blankr():
        return {"docs": set(), "labs": set(), "first": None, "type": "?", "domain": "?",
                "status": "not_in_harbor", "on": False, "registered": True}
    rise = collections.defaultdict(blankr)
    unresolved = 0

    for r in rows:
        c = r["benchmark_canonical"]
        w = w_of(r); d = r["source_doc"].get("pub_date")
        on = bool(r.get("on_harbor")); st = r.get("harbor_status", "not_in_harbor")
        lab = r["citing_lab"]; url = r["source_doc"]["url"]; typ = r.get("type"); dom = r.get("domain")

        rk = c if c else "~" + r["benchmark_raw"].strip().lower()[:40]
        rr = rise[rk]
        rr["docs"].add(url); rr["labs"].add(lab); rr["registered"] = bool(c)
        rr["type"] = typ or "?"; rr["domain"] = dom or "?"; rr["status"] = st; rr["on"] = rr["on"] or on
        if d and (rr["first"] is None or d < rr["first"]):
            rr["first"] = d
        if not c:
            unresolved += 1
            continue
        a = agg[c]
        a["w"] += w; a["cites"] += 1; a["docs"].add(url); a["labs"].add(lab)
        if r.get("weight_class") == "blog_headliner":
            a["blogs"] += 1
        else:
            a["cards"] += 1
        a["type"] = typ or "?"; a["domain"] = dom or "?"; a["status"] = st; a["on"] = a["on"] or on
        if r.get("methodology_deviations"):
            a["method"] = True
        if d and (a["first"] is None or d < a["first"]):
            a["first"] = d
        if d and d >= w90:
            a["recent"] += 1
        rep = r.get("reported") or {}
        if rep.get("unit") == "percent" and isinstance(rep.get("value"), (int, float)):
            m = r.get("citing_model") or lab
            a["model_pct"][m] = max(a["model_pct"].get(m, 0), rep["value"])
        for win, since in (("all", None), ("180", w180), ("90", w90)):
            if since is None or (d and d >= since):
                b = cov[win]; b["wt"] += w; b["n"] += 1
                if on:
                    b["won"] += w; b["non"] += 1
                elif st == "needs_review":
                    b["wrev"] += w
                if typ == "agentic":
                    b["ag_t"] += w; b["ag_n"] += 1; b["ag_o"] += w if on else 0
                elif typ == "chat":
                    b["st_t"] += w; b["st_n"] += 1; b["st_o"] += w if on else 0

    def maxpct(a):
        return max(a["model_pct"].values()) if a["model_pct"] else None
    def sat_models(a):
        return sorted([m for m, v in a["model_pct"].items() if v >= SAT])

    ranked = sorted(agg.items(), key=lambda kv: kv[1]["w"], reverse=True)
    rising = sorted([(k, v) for k, v in rise.items() if len(v["docs"]) >= 2 and v["first"] and v["first"] >= w180],
                    key=lambda kv: (len(kv[1]["docs"]), kv[1]["first"]), reverse=True)
    satur = sorted([(c, a) for c, a in agg.items() if len(sat_models(a)) >= 2],
                   key=lambda kv: (len(sat_models(kv[1])), maxpct(kv[1]) or 0), reverse=True)
    news = sorted([n for n in NEWS if n[0] >= news_cut], reverse=True)
    e = html.escape

    def bname(canon, extra=""):
        return (f"<a class=bm href='{blink(canon)}' target=_blank rel=noopener "
                f"title='see citation records for {e(canon)} on GitHub'><code>{e(canon)}</code>{extra}</a>")
    def gear(a):
        if not a["method"]:
            return ""
        return (f" <span class=gear title='harness/config-sensitive \u2014 methodology deviations on record "
                f"(e.g. different harness/scaffold/effort). Click the name to see them.'>{GEAR}</span>")
    def tspan(t):
        return f"<span class='{'t-ag' if t=='agentic' else 't-st'}'>{e(t)}</span>"
    def dspan(d):
        return f"<span class=dom>{e(d)}</span>"
    def hspan(st):
        if st == "confirmed":
            return "<span class=on>\u2713 harbor</span>"
        if st == "needs_review":
            return "<span class=rev>\u25d0 review</span>"
        return "<span class=off>\u2717 none</span>"
    def pct(v):
        return EMDASH if v is None else f"{v:.0f}%"
    def sec(title):
        return "<div class=sec>" + e(title) + "</div>"

    T = ["<!doctype html><html lang=en><head><meta charset=utf-8>",
         "<meta name=viewport content='width=device-width,initial-scale=1'>",
         "<title>PaperTrail \u2014 benchmark citations in the LLM ecosystem</title>", """<style>
    :root{--bg:#ffffff;--panel:#eef3fb;--b:#d7e3f2;--fg:#586e75;--emph:#073642;--mut:#93a1a1;
      --blue:#268bd2;--green:#22c55e;--cyan:#2aa198;--magenta:#d33682;--orange:#cb4b16;--red:#dc322f;--yellow:#b58900;--violet:#6c71c4}
    *{box-sizing:border-box}
    body{margin:0;background:var(--bg);color:var(--fg);font:13.5px/1.5 "JetBrains Mono","SF Mono",ui-monospace,Menlo,Consolas,monospace}
    .wrap{max-width:1180px;margin:0 auto;padding:22px}
    h1{font-size:22px;color:var(--emph);margin:0 0 2px;letter-spacing:.01em}
    .context{color:var(--fg);margin:0 0 2px;font-size:13.5px}
    .asof{color:var(--mut);margin:0 0 10px;font-size:12px}
    .sec{color:var(--green);font-weight:700;margin:28px 0 8px;font-size:15px}
    .cards{display:flex;flex-wrap:wrap;gap:8px;margin:12px 0}
    .kpi{background:var(--panel);border:1px solid var(--b);border-radius:6px;padding:8px 12px;min-width:118px}
    .kpi b{font-size:18px;display:block;color:var(--emph)}.kpi.big b{color:var(--green)}
    .note{color:var(--mut);font-size:12px;margin:3px 0 8px}
    .legend,.foot{color:var(--mut);font-size:11.5px;margin:16px 0 0}.legend{margin-top:26px}
    table{width:100%;border-collapse:collapse;background:var(--panel);border:1px solid var(--b);border-radius:6px;overflow:hidden}
    th,td{padding:5px 9px;text-align:left;border-bottom:1px solid var(--b);vertical-align:top}
    th{color:var(--mut);font-weight:700;font-size:10.5px;text-transform:uppercase;letter-spacing:.02em}
    tr:last-child td{border-bottom:0}td.r,th.r{text-align:right}
    code{color:var(--blue);font-weight:600;background:none;padding:0}
    a.bm{text-decoration:none}a.bm:hover code{text-decoration:underline}
    .gear{color:var(--yellow);cursor:help}
    .t-ag{color:var(--green);font-weight:700}.t-st{color:var(--violet)}
    .dom{color:var(--cyan)}.on{color:var(--green);font-weight:600}.off{color:var(--red)}.rev{color:var(--yellow)}
    .why{color:var(--mut)}.warn{color:var(--orange);font-weight:700}
    .news{background:var(--panel);border:1px solid var(--b);border-radius:6px}
    .news .row{padding:6px 12px;border-bottom:1px solid var(--b)}.news .row:last-child{border-bottom:0}
    .news .d{color:var(--mut)}.news .src{color:var(--magenta);font-weight:700}
    .news a{color:var(--blue);font-weight:600;text-decoration:none}.news a:hover{text-decoration:underline}
    .news .w{color:var(--fg)}
    .bar{height:8px;width:60px;border-radius:2px;background:#dbe7f5;display:inline-block;vertical-align:middle;margin-right:6px;border:1px solid var(--b)}
    .bar>i{display:block;height:100%;background:var(--green)}
    a{color:var(--blue)}
    </style></head><body><div class=wrap>"""]

    # header
    T.append("<h1>PaperTrail</h1>")
    T.append("<p class=context>Tracks benchmark citations in the LLM ecosystem.</p>")
    T.append(f"<p class=asof>Updated {asof}. Citations are source-weighted \u2014 a blog headliner counts 3, "
             "a model-card table 2, a system-card row 1. Click any benchmark for its underlying records.</p>")

    ag_all = cov['all']['ag_o'] / cov['all']['ag_t'] if cov['all']['ag_t'] else 0

    # What's new
    if news:
        T.append(sec("WHAT'S NEW"))
        T.append("<div class=news>")
        for dt, src, title, url, why in news:
            T.append(f"<div class=row><span class=d>[{e(dt)}]</span> <span class=src>{e(src)}</span> "
                     f"<a href='{e(url)}' target=_blank rel=noopener>{e(title)}</a><br><span class=w>{e(why)}</span></div>")
        T.append("</div>")

    # 1/ Top cited benchmarks
    T.append(sec("1/ TOP CITED BENCHMARKS"))
    T.append("<table><tr><th>#</th><th>benchmark</th><th>type</th><th>domain</th><th class=r>cards</th>"
             "<th class=r>blogs</th><th class=r>weighted</th><th class=r>labs</th><th class=r>max</th><th>harbor</th></tr>")
    for i, (c, a) in enumerate(ranked[:30], 1):
        T.append(f"<tr><td>{i}</td><td>{bname(c, gear(a))}</td><td>{tspan(a['type'])}</td><td>{dspan(a['domain'])}</td>"
                 f"<td class=r>{a['cards']}</td><td class=r>{a['blogs']}</td><td class=r>{a['w']:.1f}</td><td class=r>{len(a['labs'])}</td>"
                 f"<td class=r>{pct(maxpct(a))}</td><td>{hspan(a['status'])}</td></tr>")
    T.append("</table>")

    # 2/ Harbor coverage
    T.append(sec("2/ HARBOR COVERAGE"))
    T.append("<table><tr><th>window</th><th class=r>citations</th><th class=r>overall</th>"
             "<th class=r>agentic</th><th class=r>chat</th></tr>")
    lw = {"all": "all-time", "180": "last 6 months", "90": "last 90 days"}
    for k in ("all", "180", "90"):
        b = cov[k]
        co = b["won"]/b["wt"]*100 if b["wt"] else 0
        ag = b["ag_o"]/b["ag_t"]*100 if b["ag_t"] else 0
        stt = b["st_o"]/b["st_t"]*100 if b["st_t"] else 0
        T.append(f"<tr><td>{lw[k]}</td><td class=r>{b['n']}</td>"
                 f"<td class=r><span class=bar><i style='width:{int(co)}%'></i></span>{co:.0f}%</td>"
                 f"<td class=r>{ag:.0f}%</td>"
                 f"<td class=r>{stt:.0f}%</td></tr>")
    T.append("</table>")

    # 3/ Rising citations
    T.append(sec("3/ RISING CITATIONS"))
    T.append("<table><tr><th>benchmark</th><th>type</th><th>domain</th><th class=r>citations</th><th class=r>labs</th>"
             "<th>first seen</th><th>harbor</th></tr>")
    for k, v in rising[:20]:
        name = k[1:] if k.startswith("~") else k
        flag = "" if v["registered"] else " <span class=warn>\u26a0</span>"
        tb = tspan(v["type"]) if v["type"] in ("agentic", "chat") else "?"
        db = dspan(v["domain"]) if v["registered"] else "<span class=warn>new</span>"
        hb = hspan(v["status"]) if v["registered"] else "<span class=off>\u2717 unregistered</span>"
        first = e(v["first"] or EMDASH)
        namecell = bname(name, flag) if v["registered"] else f"<code>{e(name)}</code>{flag}"
        T.append(f"<tr><td>{namecell}</td><td>{tb}</td><td>{db}</td><td class=r>{len(v['docs'])}</td>"
                 f"<td class=r>{len(v['labs'])}</td><td>{first}</td><td>{hb}</td></tr>")
    T.append("</table>")

    # 4/ Rising saturation
    T.append(sec("4/ RISING SATURATION"))
    T.append("<table><tr><th>benchmark</th><th>type</th><th>domain</th><th class=r>models \u226580%</th>"
             "<th class=r>max</th><th>harbor</th><th>models</th></tr>")
    for c, a in satur[:20]:
        sm = sat_models(a); more = ELLIPSIS if len(sm) > 5 else ""
        T.append(f"<tr><td>{bname(c, gear(a))}</td><td>{tspan(a['type'])}</td><td>{dspan(a['domain'])}</td>"
                 f"<td class=r>{len(sm)}</td><td class=r>{maxpct(a):.0f}%</td><td>{hspan(a['status'])}</td>"
                 f"<td class=why>{e(', '.join(sm[:5]))}{more}</td></tr>")
    T.append("</table>")

    # 5/ Rising agentic vs chat
    T.append(sec("5/ RISING AGENTIC VS CHAT"))
    T.append("<table><tr><th>window</th><th class=r>citations</th><th class=r>agentic</th><th class=r>chat</th></tr>")
    def ag_share(b):
        tot = b["ag_n"] + b["st_n"]
        return (b["ag_n"] / tot * 100 if tot else 0)
    for k in ("all", "180", "90"):
        b = cov[k]; tot = b["ag_n"] + b["st_n"]; ags = ag_share(b); chs = 100 - ags if tot else 0
        T.append(f"<tr><td>{lw[k]}</td><td class=r>{tot}</td>"
                 f"<td class=r>{ags:.0f}%</td>"
                 f"<td class=r>{chs:.0f}%</td></tr>")
    T.append("</table>")

    # 6/ Harbor adapters - usage over time (for the Harbor team: is the adapter effort paying off?)
    try:
        snap = json.loads((ROOT / "data" / "harbor_adapters.json").read_text())
    except Exception:
        snap = {"adapters": [], "fetched_at": "?"}
    adsnap = {a["name"]: a for a in snap.get("adapters", [])}

    def quarter(dt):
        y, m = int(dt[:4]), int(dt[5:7])
        return f"{y}Q{(m - 1) // 3 + 1}"

    def blankad():
        return {"canon": None, "w": 0.0, "cites": 0, "labs": set(), "docs": set(),
                "first": None, "last": None, "q": collections.Counter(), "in_reg": False}
    adg = collections.defaultdict(blankad)
    for nm, meta in adsnap.items():                 # seed ALL adapters (incl. never-cited)
        adg[nm]["in_reg"] = bool(meta.get("in_registry"))
    for r in rows:
        adp = r.get("harbor_adapter")
        if not adp:
            continue
        a = adg[adp]
        a["canon"] = r["benchmark_canonical"]
        a["w"] += w_of(r); a["cites"] += 1
        a["labs"].add(r["citing_lab"]); a["docs"].add(r["source_doc"]["url"])
        a["in_reg"] = bool(r.get("harbor_in_registry"))
        d = r["source_doc"].get("pub_date")
        if d:
            a["q"][quarter(d)] += 1
            if a["first"] is None or d < a["first"]:
                a["first"] = d
            if a["last"] is None or d > a["last"]:
                a["last"] = d
    qs = sorted({q for a in adg.values() for q in a["q"]})
    cited_ad = sorted([(n, a) for n, a in adg.items() if a["cites"] > 0], key=lambda kv: (-kv[1]["w"], kv[0]))
    never_ad = sorted([(n, a) for n, a in adg.items() if a["cites"] == 0])
    n_total, n_cited, n_never = len(adg), len(cited_ad), len(never_ad)

    T.append(sec("6/ HARBOR ADAPTERS"))
    qh = "".join(f"<th class=r>{e(q)}</th>" for q in qs)
    T.append("<table><tr><th>adapter</th><th>benchmark</th>" + qh +
             "<th class=r>cites</th><th class=r>weighted</th><th class=r>labs</th><th>first\u2192last</th></tr>")
    for name, a in cited_ad:
        qcells = "".join(f"<td class=r>{(a['q'].get(q,0) or EMDASH)}</td>" for q in qs)
        canoncell = bname(a["canon"]) if a["canon"] else EMDASH
        span = (e(a["first"]) + "\u2192" + e(a["last"])) if a["first"] else EMDASH
        T.append(f"<tr><td><code>{e(name)}</code></td><td>{canoncell}</td>{qcells}"
                 f"<td class=r>{a['cites']}</td><td class=r>{a['w']:.1f}</td><td class=r>{len(a['labs'])}</td><td>{span}</td></tr>")
    T.append("</table>")
    circ = " \u25cb"
    nl = ", ".join(e(n) + ("" if a["in_reg"] else circ) for n, a in never_ad)
    T.append(f"<details><summary class=note>{n_never} adapters never cited by in-scope labs in-window "
             f"(candidates to reassess build effort) \u2014 click to expand. <span class=rev>\u25cb</span> = not in registry.json.</summary>"
             f"<p class=why>{nl}</p></details>")
    T.append("<p class=note><a href='coverage.html'>\u2192 coverage matrix</a></p>")

    T.append("<p class=legend>Key: <span class=gear>\u2699</span> methodology-sensitive (differing harness/scaffold) \u00b7 "
             "<span class=on>\u2713</span> on Harbor \u00b7 <span class=rev>\u25d0</span> in Harbor, version unconfirmed \u00b7 "
             "<span class=off>\u2717</span> not in Harbor \u00b7 <b>citations</b> = raw count \u00b7 "
             "<b>cards</b> / <b>blogs</b> = that count split by model/system cards vs headline blogs \u00b7 "
             "<b>weighted</b> scores each mention by source class: blog headliner 3, model card 2, system card 1.</p>")
    T.append("<p class=foot>christicode/benchmark-citations \u00b7 Harbor cross-referenced live \u00b7 rebuilt on every data change</p>")
    T.append("</div></body></html>")

    out = ROOT / "docs" / "index.html"
    out.parent.mkdir(exist_ok=True)
    out.write_text("\n".join(T))
    cov_tot, cov_c, cov_a, cov_r = _build_coverage(rows, ROOT, e)
    ags_all = ag_share(cov['all']); ags_90 = ag_share(cov['90'])
    print(f"wrote {out} | {len(rows)} cites, {len(agg)} benchmarks | news {len(news)} | "
          f"rising {len(rising)}, saturating {len(satur)} | agentic Harbor cov = {ag_all*100:.0f}% | agentic citation share all/90 = {ags_all:.0f}%/{ags_90:.0f}%")


