#!/usr/bin/env python3
"""Generate the static trends dashboard (docs/index.html) for GitHub Pages (autobenchmark.ai).

Site title: PaperTrail. Audience: Harbor creators (which benchmarks to build adapters for) + eng.
Two orthogonal axes per benchmark: type (agentic|chat; Harbor prefers agentic) and domain.
Sections: What's new (7d) | 1 Top cited | 2 Harbor coverage | 3 Top Harbor conversions |
          4 Rising citations | 5 Rising saturation | 6 Rising agentic vs chat.
Every benchmark name links to its citation records on GitHub. Light-terminal / syntax styling,
no external deps. Regenerated by the `regenerate` Action.
"""
from __future__ import annotations
import json, pathlib, collections, datetime, html, urllib.parse

ROOT = pathlib.Path(__file__).resolve().parents[1]
REPO = "christicode/benchmark-citations"
TABLE_NORM = 12.0
SAT = 80.0
NEW_DAYS = 180
EMDASH = "\u2014"
ELLIPSIS = "\u2026"
GEAR = "\u2699"
Q = chr(34)  # double-quote, for building GitHub code-search URLs without f-string backslashes

# Highest-signal headlines (human-curated; the daily watcher appends). Real items only.
# (date, source, title, url, why-it-matters)
NEWS = [
    ("2026-07-02", "anthropic", "More detail on Fable 5 cyber safeguards + a jailbreak-severity framework",
     "https://www.anthropic.com/news/fable-safeguards-jailbreak-framework",
     "Cross-lab (Amazon/Microsoft/Google) jailbreak scoring \u2014 safety-eval methodology to track."),
    ("2026-06-30", "openai", "Introducing GeneBench-Pro",
     "https://openai.com/index/introducing-genebench-pro/",
     "New agentic genomics / quant-bio eval \u2014 fresh benchmark to normalize + weigh for a Harbor adapter."),
    ("2026-06-30", "anthropic", "Introducing Claude Sonnet 5",
     "https://www.anthropic.com/news/claude-sonnet-5",
     "Frontier release; its 35-benchmark system card is already ingested."),
    ("2026-06-30", "anthropic", "Redeploying Fable 5",
     "https://www.anthropic.com/news/redeploying-fable-5",
     "Fable 5 / Mythos 5 back online Jul 1."),
]


def pweight(p):
    t = p.get("type")
    if t == "headline":
        return 3.0
    if t in ("footnote", "prose"):
        return 0.5
    return min(1.0, TABLE_NORM / max(p.get("table_total") or 1, 1))


def blink(canon):
    """Clickable benchmark -> its citation records on GitHub (code search surfaces the
    data/citations.jsonl lines + the curated build_citations.py entries for this name)."""
    query = "repo:" + REPO + " " + Q + canon + Q
    return "https://github.com/search?q=" + urllib.parse.quote(query) + "&type=code"


def load():
    return [json.loads(l) for l in open(ROOT / "data" / "citations.jsonl") if l.strip()]


def in_harbor(status, on):
    """Already has a Harbor adapter: confirmed (on_harbor) OR needs_review (present in Harbor
    by name; only the exact (name,version) is unconfirmed, #4). false_positive / not_in_harbor
    have no adapter -> genuine conversion candidates."""
    return on or status in ("confirmed", "needs_review")


def main():
    rows = load()
    dated = [r["source_doc"]["pub_date"] for r in rows if r["source_doc"].get("pub_date")]
    asof = max([datetime.date.today().isoformat()] + dated)
    ad = datetime.date.fromisoformat(asof)
    w90 = (ad - datetime.timedelta(days=90)).isoformat()
    w180 = (ad - datetime.timedelta(days=NEW_DAYS)).isoformat()
    news_cut = (ad - datetime.timedelta(days=7)).isoformat()

    def blank():
        return {"w": 0.0, "docs": set(), "labs": set(), "type": "?", "domain": "?", "status": "not_in_harbor",
                "on": False, "first": None, "recent": 0, "model_pct": {}, "method": False}
    agg = collections.defaultdict(blank)
    cov = {k: {"wt": 0.0, "won": 0.0, "wrev": 0.0, "n": 0, "non": 0,
               "ag_t": 0.0, "ag_o": 0.0, "st_t": 0.0, "st_o": 0.0, "ag_n": 0, "st_n": 0}
           for k in ("all", "180", "90")}

    def blankr():
        return {"docs": set(), "labs": set(), "first": None, "type": "?", "domain": "?",
                "status": "not_in_harbor", "on": False, "registered": True}
    rise = collections.defaultdict(blankr)
    unresolved = 0

    for r in rows:
        c = r["benchmark_canonical"]
        w = pweight(r["prominence"]); d = r["source_doc"].get("pub_date")
        on = bool(r.get("on_harbor")); st = r.get("harbor_status", "not_in_harbor")
        lab = r["citing_lab"]; url = r["source_doc"]["url"]; typ = r.get("type"); dom = r.get("domain")

        rk = c if c else "~" + r["benchmark_raw"].strip().lower()[:40]
        rr = rise[rk]
        rr["docs"].add(url); rr["labs"].add(lab); rr["registered"] = bool(c)
        rr["type"] = typ or "?"; rr["domain"] = dom or "?"; rr["status"] = st; rr["on"] = rr["on"] or on
        if d and (rr["first"] is None or d < rr["first"]):
            rr["first"] = d
        if not c:
            unresolved += 1
            continue
        a = agg[c]
        a["w"] += w; a["docs"].add(url); a["labs"].add(lab)
        a["type"] = typ or "?"; a["domain"] = dom or "?"; a["status"] = st; a["on"] = a["on"] or on
        if r.get("methodology_deviations"):
            a["method"] = True
        if d and (a["first"] is None or d < a["first"]):
            a["first"] = d
        if d and d >= w90:
            a["recent"] += 1
        rep = r.get("reported") or {}
        if rep.get("unit") == "percent" and isinstance(rep.get("value"), (int, float)):
            m = r.get("citing_model") or lab
            a["model_pct"][m] = max(a["model_pct"].get(m, 0), rep["value"])
        for win, since in (("all", None), ("180", w180), ("90", w90)):
            if since is None or (d and d >= since):
                b = cov[win]; b["wt"] += w; b["n"] += 1
                if on:
                    b["won"] += w; b["non"] += 1
                elif st == "needs_review":
                    b["wrev"] += w
                if typ == "agentic":
                    b["ag_t"] += w; b["ag_n"] += 1; b["ag_o"] += w if on else 0
                elif typ == "chat":
                    b["st_t"] += w; b["st_n"] += 1; b["st_o"] += w if on else 0

    def maxpct(a):
        return max(a["model_pct"].values()) if a["model_pct"] else None
    def sat_models(a):
        return sorted([m for m, v in a["model_pct"].items() if v >= SAT])

    ranked = sorted(agg.items(), key=lambda kv: kv[1]["w"], reverse=True)
    build_next = [(c, a) for c, a in ranked if not in_harbor(a["status"], a["on"])]
    rising = sorted([(k, v) for k, v in rise.items() if len(v["docs"]) >= 2 and v["first"] and v["first"] >= w180],
                    key=lambda kv: (len(kv[1]["docs"]), kv[1]["first"]), reverse=True)
    satur = sorted([(c, a) for c, a in agg.items() if len(sat_models(a)) >= 2],
                   key=lambda kv: (len(sat_models(kv[1])), maxpct(kv[1]) or 0), reverse=True)
    type_w = collections.Counter()
    for c, a in agg.items():
        type_w[a["type"]] += a["w"]
    news = sorted([n for n in NEWS if n[0] >= news_cut], reverse=True)
    e = html.escape

    def bname(canon, extra=""):
        return (f"<a class=bm href='{blink(canon)}' target=_blank rel=noopener "
                f"title='see citation records for {e(canon)} on GitHub'><code>{e(canon)}</code>{extra}</a>")
    def gear(a):
        if not a["method"]:
            return ""
        return (f" <span class=gear title='harness/config-sensitive \u2014 methodology deviations on record "
                f"(e.g. different harness/scaffold/effort). Click the name to see them.'>{GEAR}</span>")
    def tspan(t):
        return f"<span class='{'t-ag' if t=='agentic' else 't-st'}'>{e(t)}</span>"
    def dspan(d):
        return f"<span class=dom>{e(d)}</span>"
    def hspan(st):
        if st == "confirmed":
            return "<span class=on>\u2713 harbor</span>"
        if st == "needs_review":
            return "<span class=rev>\u25d0 review</span>"
        if st == "false_positive":
            return "<span class=off>\u2717 none*</span>"
        return "<span class=off>\u2717 none</span>"
    def pct(v):
        return EMDASH if v is None else f"{v:.0f}%"
    def sec(title, width=82):
        s = "== " + title + " "
        return "<div class=sec>" + e(s + "=" * max(4, width - len(s))) + "</div>"

    T = ["<!doctype html><html lang=en><head><meta charset=utf-8>",
         "<meta name=viewport content='width=device-width,initial-scale=1'>",
         "<title>PaperTrail \u2014 benchmark citations in the LLM ecosystem</title>", """<style>
    :root{--bg:#fdf6e3;--panel:#eee8d5;--b:#ddd6c1;--fg:#586e75;--emph:#073642;--mut:#93a1a1;
      --blue:#268bd2;--green:#859900;--cyan:#2aa198;--magenta:#d33682;--orange:#cb4b16;--red:#dc322f;--yellow:#b58900;--violet:#6c71c4}
    *{box-sizing:border-box}
    body{margin:0;background:var(--bg);color:var(--fg);font:13.5px/1.5 "JetBrains Mono","SF Mono",ui-monospace,Menlo,Consolas,monospace}
    .wrap{max-width:1180px;margin:0 auto;padding:22px}
    h1{font-size:22px;color:var(--emph);margin:0 0 2px;letter-spacing:.01em}
    .context{color:var(--fg);margin:0 0 2px;font-size:13.5px}
    .asof{color:var(--mut);margin:0 0 10px;font-size:12px}
    .sec{color:var(--green);font-weight:700;margin:28px 0 6px;white-space:pre;overflow:hidden;font-size:13px}
    .cards{display:flex;flex-wrap:wrap;gap:8px;margin:12px 0}
    .kpi{background:var(--panel);border:1px solid var(--b);border-radius:6px;padding:8px 12px;min-width:118px}
    .kpi b{font-size:18px;display:block;color:var(--emph)}.kpi.big b{color:var(--green)}
    .note{color:var(--mut);font-size:12px;margin:3px 0 8px}
    table{width:100%;border-collapse:collapse;background:var(--panel);border:1px solid var(--b);border-radius:6px;overflow:hidden}
    th,td{padding:5px 9px;text-align:left;border-bottom:1px solid var(--b);vertical-align:top}
    th{color:var(--mut);font-weight:700;font-size:10.5px;text-transform:uppercase;letter-spacing:.02em}
    tr:last-child td{border-bottom:0}td.r,th.r{text-align:right}
    code{color:var(--blue);font-weight:600;background:none;padding:0}
    a.bm{text-decoration:none}a.bm:hover code{text-decoration:underline}
    .gear{color:var(--yellow);cursor:help}
    .t-ag{color:var(--green);font-weight:700}.t-st{color:var(--violet)}
    .dom{color:var(--cyan)}.on{color:var(--green);font-weight:600}.off{color:var(--red)}.rev{color:var(--yellow)}
    .why{color:var(--mut)}.warn{color:var(--orange);font-weight:700}
    .news{background:var(--panel);border:1px solid var(--b);border-radius:6px}
    .news .row{padding:6px 12px;border-bottom:1px solid var(--b)}.news .row:last-child{border-bottom:0}
    .news .d{color:var(--mut)}.news .src{color:var(--magenta);font-weight:700}
    .news a{color:var(--blue);font-weight:600;text-decoration:none}.news a:hover{text-decoration:underline}
    .news .w{color:var(--fg)}
    .bar{height:8px;width:60px;border-radius:2px;background:#e4dcc4;display:inline-block;vertical-align:middle;margin-right:6px;border:1px solid var(--b)}
    .bar>i{display:block;height:100%;background:var(--green)}
    a{color:var(--blue)}
    </style></head><body><div class=wrap>"""]

    # header
    T.append("<h1>PaperTrail</h1>")
    T.append("<p class=context>Tracks benchmark citations in the LLM ecosystem.</p>")
    T.append(f"<p class=asof>updated {asof} \u00b7 prominence-weighted (headline blog = 3\u00d7 a deep table row) \u00b7 "
             "click any benchmark to see its citation records on GitHub</p>")

    cov_conf = cov['all']['won'] / cov['all']['wt'] if cov['all']['wt'] else 0
    ag_all = cov['all']['ag_o'] / cov['all']['ag_t'] if cov['all']['ag_t'] else 0
    T.append("<div class=cards>")
    for lab, val, big in [("citations", len(rows), 0), ("benchmarks", len(agg), 0),
                          ("source docs", len({r['source_doc']['url'] for r in rows}), 0),
                          ("agentic / chat", f"{type_w['agentic']:.0f} / {type_w['chat']:.0f}", 0),
                          ("on Harbor (wtd)", f"{cov_conf*100:.0f}%", 0),
                          ("AGENTIC on Harbor", f"{ag_all*100:.0f}%", 1)]:
        T.append(f"<div class='kpi{' big' if big else ''}'><b>{val}</b>{lab}</div>")
    T.append("</div>")

    # What's new
    if news:
        T.append(sec("WHAT'S NEW \u00b7 LAST 7 DAYS"))
        T.append("<div class=news>")
        for dt, src, title, url, why in news:
            T.append(f"<div class=row><span class=d>[{e(dt)}]</span> <span class=src>{e(src)}</span> "
                     f"<a href='{e(url)}' target=_blank rel=noopener>{e(title)}</a><br><span class=w>{e(why)}</span></div>")
        T.append("</div>")

    # 1/ Top cited benchmarks
    T.append(sec("1/ TOP CITED BENCHMARKS \u00b7 regardless of Harbor status"))
    T.append("<table><tr><th>#</th><th>benchmark</th><th>type</th><th>domain</th><th class=r>wtd cites</th>"
             "<th class=r>docs</th><th class=r>labs</th><th class=r>max</th><th>harbor</th></tr>")
    for i, (c, a) in enumerate(ranked[:30], 1):
        T.append(f"<tr><td>{i}</td><td>{bname(c, gear(a))}</td><td>{tspan(a['type'])}</td><td>{dspan(a['domain'])}</td>"
                 f"<td class=r>{a['w']:.1f}</td><td class=r>{len(a['docs'])}</td><td class=r>{len(a['labs'])}</td>"
                 f"<td class=r>{pct(maxpct(a))}</td><td>{hspan(a['status'])}</td></tr>")
    T.append(f"</table><p class=note># {GEAR} = harness/config-sensitive (methodology deviations on record). "
             f"Click any benchmark to open its citation records on GitHub. {len(ranked)} benchmarks; top 30 shown.</p>")

    # 2/ Harbor coverage
    T.append(sec("2/ HARBOR COVERAGE \u00b7 share of citations runnable today"))
    T.append("<p class=note># prominence-weighted; split by type because Harbor prefers agentic \u2014 the agentic column is the one that matters.</p>")
    T.append("<table><tr><th>window</th><th class=r>citations</th><th class=r>overall</th>"
             "<th class=r>agentic</th><th class=r>chat</th><th class=r>if \u25d0 confirmed</th></tr>")
    lw = {"all": "all-time", "180": "last 6 months", "90": "last 90 days"}
    for k in ("all", "180", "90"):
        b = cov[k]
        co = b["won"]/b["wt"]*100 if b["wt"] else 0
        ag = b["ag_o"]/b["ag_t"]*100 if b["ag_t"] else 0
        stt = b["st_o"]/b["st_t"]*100 if b["st_t"] else 0
        cp = (b["won"]+b["wrev"])/b["wt"]*100 if b["wt"] else 0
        T.append(f"<tr><td>{lw[k]}</td><td class=r>{b['n']}</td>"
                 f"<td class=r><span class=bar><i style='width:{int(co)}%'></i></span>{co:.0f}%</td>"
                 f"<td class=r><span class=bar><i style='width:{int(ag)}%'></i></span>{ag:.0f}%</td>"
                 f"<td class=r>{stt:.0f}%</td><td class=r>{cp:.0f}%</td></tr>")
    T.append("</table>")
    tr90 = (cov['90']['ag_o']/cov['90']['ag_t'] if cov['90']['ag_t'] else 0) - (cov['all']['ag_o']/cov['all']['ag_t'] if cov['all']['ag_t'] else 0)
    msg = ("agentic coverage is holding." if tr90 >= 0 else
           "agentic coverage is <b>lower on recent citations</b> \u2014 new agentic benchmarks are outrunning Harbor; see Top Harbor conversions.")
    T.append(f"<p class=note># {msg} \u25d0 = in Harbor by name, (name,version) unconfirmed (#4). "
             f"{unresolved} citations excluded (unidentified / needs-review names \u2014 #8, #9).</p>")

    # 3/ Top Harbor conversions
    T.append(sec("3/ TOP HARBOR CONVERSIONS \u00b7 most-cited, no adapter yet"))
    T.append("<p class=note># adapter shortlist \u2014 high citation demand, genuinely NOT in Harbor. "
             "Already-in-Harbor benchmarks (confirmed <span class=on>\u2713</span> and version-unconfirmed "
             "<span class=rev>\u25d0</span>, e.g. terminal-bench) are excluded. * = an earlier auto-match was a false positive.</p>")
    T.append("<table><tr><th>#</th><th>benchmark</th><th>type</th><th>domain</th><th class=r>wtd cites</th>"
             "<th class=r>docs</th><th class=r>labs</th><th class=r>max</th><th>harbor</th><th>why now</th></tr>")
    for i, (c, a) in enumerate(build_next[:15], 1):
        mx = maxpct(a); why = ["rising" if a["recent"] >= 2 else f"{len(a['docs'])} docs"]
        if mx is not None and mx >= SAT: why.append(f"saturating {mx:.0f}%")
        elif mx is not None: why.append(f"headroom (max {mx:.0f}%)")
        else: why.append("no score yet")
        if a["type"] == "agentic": why.append("agentic \u2605")
        if a["status"] == "false_positive": why.append("name collides w/ Harbor entry")
        T.append(f"<tr><td>{i}</td><td>{bname(c, gear(a))}</td><td>{tspan(a['type'])}</td><td>{dspan(a['domain'])}</td>"
                 f"<td class=r>{a['w']:.1f}</td><td class=r>{len(a['docs'])}</td><td class=r>{len(a['labs'])}</td>"
                 f"<td class=r>{pct(mx)}</td><td>{hspan(a['status'])}</td><td class=why>{e('; '.join(why))}</td></tr>")
    T.append(f"</table><p class=note># {len(build_next)} benchmarks not in Harbor; top 15 by prominence-weighted demand.</p>")

    # 4/ Rising citations
    T.append(sec("4/ RISING CITATIONS \u00b7 new entrants gaining citations"))
    T.append(f"<p class=note># first cited since {w180}, already in \u22652 docs. <span class=warn>\u26a0</span> = not yet in the alias registry (#8).</p>")
    T.append("<table><tr><th>benchmark</th><th>type</th><th>domain</th><th class=r>docs</th><th class=r>labs</th>"
             "<th>first seen</th><th>harbor</th></tr>")
    for k, v in rising[:20]:
        name = k[1:] if k.startswith("~") else k
        flag = "" if v["registered"] else " <span class=warn>\u26a0</span>"
        tb = tspan(v["type"]) if v["type"] in ("agentic", "chat") else "?"
        db = dspan(v["domain"]) if v["registered"] else "<span class=warn>new</span>"
        hb = hspan(v["status"]) if v["registered"] else "<span class=off>\u2717 unregistered</span>"
        first = e(v["first"] or EMDASH)
        namecell = bname(name, flag) if v["registered"] else f"<code>{e(name)}</code>{flag}"
        T.append(f"<tr><td>{namecell}</td><td>{tb}</td><td>{db}</td><td class=r>{len(v['docs'])}</td>"
                 f"<td class=r>{len(v['labs'])}</td><td>{first}</td><td>{hb}</td></tr>")
    T.append("</table>")

    # 5/ Rising saturation
    T.append(sec("5/ RISING SATURATION \u00b7 climbing toward ceiling"))
    T.append(f"<p class=note># \u22652 distinct models report \u2265{SAT:.0f}% (percent-metric only). high saturation = shorter useful life.</p>")
    T.append("<table><tr><th>benchmark</th><th>type</th><th>domain</th><th class=r>models \u226580%</th>"
             "<th class=r>max</th><th>harbor</th><th>models</th></tr>")
    for c, a in satur[:20]:
        sm = sat_models(a); more = ELLIPSIS if len(sm) > 5 else ""
        T.append(f"<tr><td>{bname(c, gear(a))}</td><td>{tspan(a['type'])}</td><td>{dspan(a['domain'])}</td>"
                 f"<td class=r>{len(sm)}</td><td class=r>{maxpct(a):.0f}%</td><td>{hspan(a['status'])}</td>"
                 f"<td class=why>{e(', '.join(sm[:5]))}{more}</td></tr>")
    T.append("</table>")

    # 6/ Rising agentic vs chat
    T.append(sec("6/ RISING AGENTIC VS CHAT \u00b7 citation mix over time"))
    T.append("<p class=note># prominence-weighted share of classified citations that are agentic vs chat. "
             "Harbor prefers agentic, so a rising agentic share is a leading signal for adapter demand.</p>")
    T.append("<table><tr><th>window</th><th class=r>citations</th><th class=r>agentic</th><th class=r>chat</th>"
             "<th class=r>agentic share</th></tr>")
    def share(b):
        tot = b["ag_t"] + b["st_t"]
        return (b["ag_t"] / tot * 100 if tot else 0)
    for k in ("all", "180", "90"):
        b = cov[k]; sh = share(b)
        T.append(f"<tr><td>{lw[k]}</td><td class=r>{b['ag_n'] + b['st_n']}</td>"
                 f"<td class=r>{b['ag_n']} <span class=t-ag>({b['ag_t']:.0f} wtd)</span></td>"
                 f"<td class=r>{b['st_n']} <span class=t-st>({b['st_t']:.0f} wtd)</span></td>"
                 f"<td class=r><span class=bar><i style='width:{int(sh)}%'></i></span>{sh:.0f}%</td></tr>")
    T.append("</table>")
    sh_all, sh_90 = share(cov['all']), share(cov['90'])
    delta = sh_90 - sh_all
    if abs(delta) < 1:
        smsg = f"agentic share is holding near {sh_all:.0f}% (90-day {sh_90:.0f}% vs all-time {sh_all:.0f}%)."
    elif delta > 0:
        smsg = f"agentic share is <b>rising</b>: {sh_90:.0f}% of the last 90 days vs {sh_all:.0f}% all-time \u2014 the labs are citing more agentic evals."
    else:
        smsg = f"agentic share is <b>falling</b>: {sh_90:.0f}% of the last 90 days vs {sh_all:.0f}% all-time."
    T.append(f"<p class=note># {smsg}</p>")

    T.append("<p class=note style='margin-top:24px'># source of truth: <code>christicode/benchmark-citations</code> (git) + GitHub Issues. "
             "Harbor cross-referenced live from <code>harbor-framework/harbor</code>. Rebuilt on every data change.</p>")
    T.append("</div></body></html>")

    out = ROOT / "docs" / "index.html"
    out.parent.mkdir(exist_ok=True)
    out.write_text("\n".join(T))
    print(f"wrote {out} | {len(rows)} cites, {len(agg)} benchmarks | news {len(news)} | conversions {len(build_next)}, "
          f"rising {len(rising)}, saturating {len(satur)} | agentic Harbor cov = {ag_all*100:.0f}% | agentic share all/90 = {sh_all:.0f}%/{sh_90:.0f}%")


if __name__ == "__main__":
    main()
