#!/usr/bin/env python3
"""Generate GAPS.md — a human worklist of extraction gaps, with source links.

Reads data/documents.yaml + data/citations.jsonl and lists, per document:
  * docs with NO extracted benchmarks (image-only / JS / gated) -> read the whole table;
  * cards that look incomplete (few rows for a card) -> likely missing rows;
  * benchmarks that are cited but have NO numeric score (score_pending) -> just need the number.

Each entry has the source URL and a copy-paste block ("<benchmark> = ") so a human can fill
values and hand them back; those map straight into data/extractions/<lab>/<id>.yaml. We never
guess a score — this file just makes the gaps easy to find and fill.
"""
from __future__ import annotations
import json, pathlib, collections
import yaml

ROOT = pathlib.Path(__file__).resolve().parents[1]
OUT = ROOT / "GAPS.md"
THIN = 12  # a model/system card with fewer named benchmarks than this is likely under-mined


def has_score(r):
    v = (r.get("reported") or {}).get("value")
    return isinstance(v, (int, float))


def main() -> int:
    docs = {d["id"]: d for d in yaml.safe_load((ROOT / "data" / "documents.yaml").read_text())["documents"]}
    rows = [json.loads(l) for l in open(ROOT / "data" / "citations.jsonl") if l.strip()]

    per = collections.defaultdict(lambda: {"named": [], "pending": [], "scored": [], "unread": None})
    for r in rows:
        did = r["source_doc"]["id"]
        p = per[did]
        if r.get("benchmark_canonical"):
            raw = r["benchmark_raw"]
            p["named"].append(raw)
            (p["scored"] if has_score(r) else p["pending"]).append(raw)
        elif r.get("needs_review"):
            p["unread"] = r.get("review_reason")

    def dedupe(xs):
        seen, out = set(), []
        for x in xs:
            if x not in seen:
                seen.add(x); out.append(x)
        return out

    unread, thin, scores_only = [], [], []
    for did, d in docs.items():
        p = per[did]
        n = len(dedupe(p["named"]))
        wc = d.get("default_weight_class", "?")
        is_card = wc in ("model_card", "system_card")
        if p["unread"] or n == 0:
            unread.append(did)
        elif is_card and n < THIN:
            thin.append(did)
        elif p["pending"]:
            scores_only.append(did)

    n_pending = sum(len(dedupe(per[d]["pending"])) for d in docs)
    L = []
    L.append("# Extraction gap worklist")
    L.append("")
    L.append("_Auto-generated by `scripts/gaps.py` from `data/documents.yaml` + `data/citations.jsonl`. "
             "Regenerates on every data change, so the list shrinks as gaps are filled._")
    L.append("")
    L.append(f"**{len(unread)}** docs with no data · **{len(thin)}** likely-incomplete cards · "
             f"**{n_pending}** cited benchmarks missing a score.")
    L.append("")
    L.append("### How to hand it back")
    L.append("Under any doc below, copy the code block, fill a value after each `=` "
             "(a percent like `71.2`, or an Elo/USD/raw number; leave blank if it's genuinely not "
             "on the page), and paste it back to me. I'll write it into "
             "`data/extractions/<lab>/<id>.yaml` and the site regenerates. Add a line "
             "`<benchmark> = <value>` for any row we're missing entirely.")
    L.append("")

    def block(did, names, note):
        d = docs[did]
        model = d.get("model") or did
        L.append(f"### {model} — `{d.get('default_weight_class','?')}`")
        L.append(f"[source]({d['url']}) · doc id `{did}`" + (f" · {note}" if note else ""))
        p = per[did]
        if p["scored"]:
            L.append(f"<sub>already have scores: {', '.join(dedupe(p['scored']))}</sub>")
        L.append("")
        L.append("```")
        L.append(f"# {did}  —  fill value after '='  (%/elo/usd; blank if not shown)")
        for nm in names:
            L.append(f"{nm} = ")
        L.append("```")
        L.append("")

    L.append("## 🔴 No data extracted yet — read the whole table")
    L.append("")
    for did in unread:
        r = per[did]["unread"] or "no text-extractable scores found"
        block(did, [], f"**why:** {r}")

    L.append("## 🟠 Cards that look incomplete — likely missing rows + scores")
    L.append("")
    for did in sorted(thin, key=lambda x: len(dedupe(per[x]["named"]))):
        names = dedupe(per[did]["pending"]) or dedupe(per[did]["named"])
        n = len(dedupe(per[did]["named"]))
        block(did, names, f"only **{n}** rows for a card — check for missing benchmarks too")

    L.append("## 🟡 Scores missing — cells show, but the Saturation bar is blank")
    L.append("")
    for did in sorted(scores_only, key=lambda x: -len(dedupe(per[x]["pending"]))):
        pend = dedupe(per[did]["pending"])
        block(did, pend, f"**{len(pend)}** benchmarks need a number")

    OUT.write_text("\n".join(L) + "\n")
    print(f"wrote {OUT} | {len(unread)} unreadable, {len(thin)} thin, {len(scores_only)} score-gap docs "
          f"| {n_pending} score-pending citations")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
